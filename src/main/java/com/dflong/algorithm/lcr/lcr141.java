package com.dflong.algorithm.lcr;

public class lcr141 {

    public ListNode trainningPlan(ListNode head) {
        ListNode pre = null, cur = head;
        while (cur != null) {
            ListNode next = cur.next;
            cur.next = pre;
            pre = cur;
            cur = next;
        }

        return pre;
    }
}
